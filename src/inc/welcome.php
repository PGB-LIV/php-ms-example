<h2>phpMS Demo Suite</h2>

<p>
    This resource contains MS proteome informatics utility software,
    developed within the phpMS project. The Demo Suite is intended to
    show some of the possibilities of the phpMS library. All the code is
    available as open source, hosted on <a
        href="https://github.com/PGB-LIV/php-ms-example">GitHub</a>, so
    you can embed these utilities in your own applications, or further
    develop them as needed. Please report any bugs or feature requests
    via the GitHub "issue tracker"
</p>

